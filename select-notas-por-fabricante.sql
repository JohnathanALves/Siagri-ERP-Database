SELECT 

T.RAZA_TRA AS CLIENTE,
T.CGC_TRA AS CNPJ,
N.NOTA_NOT || '-' || N.SERI_NOT AS NF,
N.DEMI_NOT AS EMISSAO,
P.DESC_PSV AS PRODUTO,
MUN.DESC_MUN AS MUNICIPIO,
MUN.ESTA_MUN AS ESTADO,
(INF.QTDE_INO - INF.QDEV_INO) AS QUANTITADE,
(INF.QTDE_INO - INF.QDEV_INO) * PD.QTE1_PRO AS QUANTIDADE_KG_L,
INF.VLOR_INO AS VALOR_UNITARIO,
INF.VLOR_INO * (INF.QTDE_INO - INF.QDEV_INO) AS VALOR_TOTAL
--P.NOME_PES
FROM NOTA N
LEFT JOIN INOTA INF ON (N.NPRE_NOT = INF.NPRE_NOT)
LEFT JOIN TRANSAC T ON (N.CODI_TRA = T.CODI_TRA)
LEFT JOIN PRODSERV P ON (INF.CODI_PSV = P.CODI_PSV)
LEFT JOIN PRODUTO PD ON (P.CODI_PSV = PD.CODI_PSV)
LEFT JOIN MUNICIPIO MUN ON (T.CODI_MUN = MUN.CODI_MUN)
LEFT JOIN FUNCAOTOPER FT ON (N.CODI_TOP = FT.CODI_TOP)
LEFT JOIN PESSOAL P ON (P.CODI_PES = N.COD1_PES)
WHERE FT.CODI_PTO = 112 AND PD.CODI_TRA IN (10005067, 1989) AND N.SITU_NOT = '5'AND
T.CODI_TRA IN (10006001, 10004672, 10005932, 10004746, 1564, 18, 1610, 10004682, 1614, 2757, 1715, 1764, 2019, 1807, 10008424, 1873, 1893, 1768, 10004683)
--N.COD1_PES IN (10000173, 10000229, 33, 10000211, 10000202, 10000247)
AND N.DEMI_NOT BETWEEN TO_DATE('01/08/2018') AND TO_DATE('31/07/2019');